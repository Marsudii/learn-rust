pub fn type_data() {
    println!("================================== TYPE DATA ==================================");
    let a: i8 = 1; // integer 8
    let b: i16 = 2; // integer 16
    let c: i32 = 3; // integer 32
    let d: i64 = 4; // integer 64
    let e: i128 = 5; // integer 128
    let f: u8 = 6; // unsigned integer 8
    let g: u16 = 7; // unsigned integer 16
    let h: u32 = 8; // unsigned integer 32
    let i: u64 = 9; // unsigned integer 64
    let j: u128 = 10; // unsigned integer 128
    let k: f32 = 11.0; // float 32
    let l: f64 = 12.0; // float 64
    let m: bool = true; // boolean
    let n: char = 'A'; // character
    println!("The value of a is: {} integer 8", a);
    println!("The value of b is: {} integer 16", b);
    println!("The value of c is: {} integer 32", c);
    println!("The value of d is: {} integer 64", d);
    println!("The value of e is: {} integer 128", e);
    println!("The value of f is: {} unsigned integer 8", f);
    println!("The value of g is: {} unsigned integer 16", g);
    println!("The value of h is: {} unsigned integer 32", h);
    println!("The value of i is: {} unsigned integer 64", i);
    println!("The value of j is: {} unsigned integer 128", j);
    println!("The value of k is: {} float 32", k);
    println!("The value of l is: {} float 64", l);
    println!("The value of m is: {} boolean", m);
    println!("The value of n is: {} character", n);

    println!("================================== TYPE DATA ==================================");
}
